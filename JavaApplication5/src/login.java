    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if(jTextField1.getText().isEmpty())
        {   
            JOptionPane.showMessageDialog(rootPane, "please enter a name ");
            return;
          
        }
         else if(jPasswordField1.getText().isEmpty())
                 { 
                     JOptionPane.showMessageDialog(rootPane, "please enter a password");
                     return;
                 }
           
        try {
                    Connection con =getConnection();
                    String q="select * from student where name=? and password=?";
                    PreparedStatement ps=con.prepareStatement(q);
                    ps.setString(1, jTextField1.getText());
                    ps.setString(2, jPasswordField1.getText());
                     ResultSet rs=ps.executeQuery();
                     
                     
                      if (rs.next())
                     {   
                         JOptionPane.showMessageDialog(rootPane, "login is valid ...");
                         
                        // setVisible(false);
                     }
                      else
                     {
                        
                         JOptionPane.showMessageDialog(rootPane, "login not valid");
                     }
                    
                     con.close();
            } catch (ClassNotFoundException ex) {
                    
              
                    ex.printStackTrace();
                } catch (SQLException ex) {
                     //int ercode = ex.getErrorCode();
                     
                    ex.printStackTrace();
                }
        
        
        
         
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

